    public com.mysql.jdbc.Connection getConnection(){
           Connection conn=null;
        try{
            Class.forName(driver);
            conn= (com.mysql.jdbc.Connection) DriverManager.getConnection(url, user, pass);
            if(conn != null){
                System.out.println("la conexion se establecion bien con la db");
            }
        }catch(ClassNotFoundException | SQLException e){
            System.out.println("Error al conectar con la db" + e);
        }
        return (com.mysql.jdbc.Connection) conn;
   
    }
    
   
